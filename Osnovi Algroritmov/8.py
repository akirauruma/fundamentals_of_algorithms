rainbow = {
    1: "красный",
    2: "оранжевый",
    3: "желтый",
    4: "зеленый",
    5: "голубой",
    6: "синий",
    7: "фиолетовый"
}

num = int(input("Введите номер цвета (1-7): "))

if num in rainbow:
    print(f"Цвет радуги с номером {num} - это {rainbow[num]}.")
else:
    print("Неверный номер.")
